package commaninterview;

public class mile3 {
public static void main(String[] args) {
	int a=8;
	int b=5;
	System.out.println("after swapping");
	System.out.println("a"+a);
	System.out.println("b"+b);
	
	
	
	System.out.println("before swapping");
	int temp;
	temp=a;
	a=b;
	b=temp;
	System.out.println("a"+a);
	System.out.println("b"+b);
}
}
