package commaninterview;

public class mile2 {
	public static void main(String[] args) {
		 int a=8;
		 int b=9;
		 System.out.println("before swapping");
		 System.out.println("a="+a);
		 System.out.println("b="+b);
		 
		 
		 
		 System.out.println("after swappping");
		 int temp;
		 temp=a;
		 a=b;
		 b=temp;
		 System.out.println("a="+a);
		 System.out.println("b="+b);
	}

}
